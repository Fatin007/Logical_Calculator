    public void decToAll(){
        int x=Integer.parseInt(s);
        bin.setText(Integer.toBinaryString(x));
        hex.setText(Integer.toHexString(x).toUpperCase());
        dec.setText(String.valueOf(x));
        oct.setText(Integer.toOctalString(x));
    }
    
    public void hexToAll(){
        int x=Integer.parseInt(s, 16);
        bin.setText(Integer.toBinaryString(x));
        dec.setText(String.valueOf(x));
        oct.setText(Integer.toOctalString(x)); 
        hex.setText(s.toUpperCase()); 
    }
    
    public void octToAll(){
        int x=Integer.parseInt(s, 8);
        bin.setText(Integer.toBinaryString(x));
        dec.setText(String.valueOf(x));
        hex.setText(Integer.toHexString(x).toUpperCase()); 
        oct.setText(s);
    }
    
    public void binToAll(){
        int x = Integer.parseInt(s, 2);
        bin.setText(s); 
        dec.setText(String.valueOf(x)); 
        hex.setText(Integer.toHexString(x).toUpperCase());
        oct.setText(Integer.toOctalString(x));
    }

    
