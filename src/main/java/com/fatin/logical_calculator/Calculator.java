    private void bFActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bFActionPerformed
        // TODO add your handling code here:
        s+="F";
        if(s.equals("0F")) s="F";
        tf.setText(s);
        switch (active) {
            case 1 -> hexToAll();
            case 2 -> decToAll();
            case 3 -> octToAll();
            case 4 -> binToAll();
        }
    }//GEN-LAST:event_bFActionPerformed

    private void bCCCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCCCActionPerformed
        // TODO add your handling code here:
        if(s.length()==1){
            s="0";
            tf.setText(s);
            hex.setText(s);
            dec.setText(s);
            oct.setText(s);
            bin.setText(s);
            return;
        }
        
        String tmp=s;
        s="";
        for(int i=0; i<tmp.length()-1; i++){
            s+=tmp.charAt(i);
        }
        tf.setText(s);
        switch (active) {
            case 1 -> hexToAll();
            case 2 -> decToAll();
            case 3 -> octToAll();
            case 4 -> binToAll();
        }
    }//GEN-LAST:event_bCCCActionPerformed

